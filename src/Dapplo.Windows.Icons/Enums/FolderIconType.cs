/// <summary>
///     Options to specify whether folders should be in the open or closed state.
/// </summary>
public enum FolderIconType
{
    /// <summary>
    ///     Specify open folder.
    /// </summary>
    Open = 0,

    /// <summary>
    ///     Specify closed folder.
    /// </summary>
    Closed = 1
}