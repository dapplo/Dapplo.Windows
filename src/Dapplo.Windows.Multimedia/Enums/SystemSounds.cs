        /// Sound/event that is associated with the Windows "Information" alert type.
        /// </summary>
        SystemAsterisk,

        /// <summary>
        /// Sound/event that is associated with the Windows "default" alert type.
        /// </summary>
        SystemDefault,

        /// <summary>
        /// Sound/event that is associated with the Windows "Exclamation" alert type.
        /// </summary>
        SystemExclamation,

        /// <summary>
        /// Sound/event that is associated with the Windows "Exit" alert type.
        /// </summary>
        SystemExit,

        /// <summary>
        /// Sound/event that is associated with the Windows "Hand" alert type.
        /// </summary>
        SystemHand,

        /// <summary>
        /// Sound/event that is associated with the Windows "Question" alert type.
        /// </summary>
        SystemQuestion,

        /// <summary>
        /// Sound/event that is associated with the Windows "Start".
        /// </summary>
        SystemStart,

        /// <summary>
        /// Sound/event that is associated with the Windows "Welcome".
        /// </summary>
        SystemWelcome
    }
}
